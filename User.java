package paymentapp.com;

public class User {
	
	String username;
	String Password;
	public String getUserName() {
		return username;
	}
	public void setUserName(String UserName) {
		UserName = UserName;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}

	
}

